package com.abaco.entidad;

public class EPlantillaEmail {

	private double codigoMsg;
	private String asunto;
	private String cuerpo;
	
	public double getCodigoMsg() {
		return codigoMsg;
	}
	
	public void setCodigoMsg(double codigoMsg) {
		this.codigoMsg = codigoMsg;
	}
	
	public String getAsunto() {
		return asunto;
	}
	
	public void setAsunto(String asunto) {
		this.asunto = asunto;
	}
	
	public String getCuerpo() {
		return cuerpo;
	}
	
	public void setCuerpo(String cuerpo) {
		this.cuerpo = cuerpo;
	}		
}
