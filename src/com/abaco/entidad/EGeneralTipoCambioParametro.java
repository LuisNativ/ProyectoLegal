package com.abaco.entidad;

public class EGeneralTipoCambioParametro {

	private int anio;
	private int mes;
	private int moneda;
	private String dia;
	
	public int getAnio() {
		return anio;
	}
	
	public void setAnio(int anio) {
		this.anio = anio;
	}
	
	public int getMes() {
		return mes;
	}
	
	public void setMes(int mes) {
		this.mes = mes;
	}
	
	public int getMoneda() {
		return moneda;
	}
	
	public void setMoneda(int moneda) {
		this.moneda = moneda;
	}
	
	public String getDia() {
		return dia;
	}
	
	public void setDia(String dia) {
		this.dia = dia;
	}
}
